package graphics.stadium;

/**
 * Enum per gestire le modalità di accesso allo Stadio.
 * 
 * @author Maurizio
 */
public enum StadiumMode {
	/**
	 * Indica l'accesso allo Stadio per la prenotazione di un biglietto.
	 */
	PRENOTAZIONE,

	/**
	 * Indica l'accesso allo Stadio per l'acquisto di un biglietto.
	 */
	ACQUISTO;
}
